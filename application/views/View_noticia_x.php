</time><address class="si-detail__author"data-uk-scrollspy="hidden: false; repeat: true; offset-top: -50">
    <br>    
                    <img src="https://www.minsalud.gob.bo/images/Noticias20/reporte-covid-282-01.jpg" alt="">
                    
                   
    <br>
                    <p>El ministro boliviano de Salud , Jeyson Auza, presentó el último informe epidemiológico semanal en el que detalló que se reportaron 1.609 casos más de la covid-19 en la última semana.</p>
                    <p>"Con esta cuarta semana consecutiva de incremento de casos hoy podemos anunciar el inicio de la sexta ola epidemiológica", indicó Auza.</p>
                    <p>El ministro de Salud atribuyo el aumento de los casos a la detección de la nueva subvariante BQ de Ómicron.</p>
                    <p>Auza señaló que en las últimas semanas se detectó la circulación en el país "de BQ.1 y BQ.1.1, que todavía no son una variante dominante", sin embargo, "sabiendo el potencial de contagio mayor que tiene esta variante no dudamos que esta se convierta en dominante en el país".</p>
                    <p>Bolivia reportó en la última jornada 192 nuevos contagios y una persona fallecida, con lo que el acumulado por la enfermedad desde que en marzo de 2020 se detectaron los primeros casos de la covid-19 se ubica en 1.112.501 y los decesos en 22.250.</p>
                    <img src="https://www.unicef.org/bolivia/sites/unicef.org.bolivia/files/styles/hero_desktop/public/bol_inmunizacion.jpg?itok=KjQnnt9K" widht="600" height="600" alt="">
                    <p>El ministerio mencionó que los síntomas de estas subvariantes son tos, dolor de garganta, fatiga, malestar general, diarrea, congestión y secreción nasal, dolor de cabeza, fiebre, dolores musculares, pérdida del olfato o el gusto, sensación de falta de aire y el aceleramiento del ritmo cardíaco.</p>
                    <p>Asimismo, Auza resaltó que la tasa de letalidad se mantiene en el 0,1 por ciento y pidió a la población mantener las medidas de bioseguridad, como también acudir a la vacunación contra la covid-19.</p>
                    <p>Sobre la inmunización, el ministerio informó que hasta la fecha se han aplicado 15.260.060 vacunas entre unidosis, primera, segunda, tercera y de refuerzo, del total de 23.815.500 componentes que ha adquirido el Gobierno desde enero de 2021.</p>
                    
                    <div class="si-grid">

                    <p>El reporte establece que se aplicó 813.441 primeras dosis 584.060 segundas dosis a niños de 5 a 11 años, mientras que 803.449 a adolescentes de 12 a 17 años en primeras dosis y 634.910 en la segunda.</p>
                    <p>El ministro Auza también se refirió a los casos de la viruela del mono que ascienden a 259, de los que 246 se han recuperado. El primer contagio se registró en el país el 1 de agosto de este año.</p>
                    <p>Sobre los nuevos casos de tosferina se detectaron 29 en la última semana y el acumulado desde mediados de octubre se ubica en 79 contagios. EFE</p>
