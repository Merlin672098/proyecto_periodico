                    Bolivia ingresa a sexta ola de la pandemia y casos por covid suben un 203 %
                    </h1>
                    <section class="si-detail__content">
                    <span class="show-for-sr">Autor: Scarley Navarro</span>
                    <br>
<span class="show-for-sr">Este contenido fue publicado el 05 diciembre 2022 - 14:50</span>
<time class="si-detail__date"datetime="2022-12-05T14:50:18+01:00">
